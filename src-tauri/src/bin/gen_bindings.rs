//! Regenerate `src/ipc/bindings.ts` from the Rust command/event surface.
//!
//! Run via `yarn gen-bindings`. Not a `build.rs` hook — we only regenerate
//! on demand so incremental Rust builds stay fast.
//!
//! The binary collects every `#[tauri::command]` annotated with
//! `#[specta::specta]` + every `tauri_specta::Event` and emits a single
//! TypeScript file containing the command/event wrappers and their types.

use std::path::PathBuf;

use buddy_lib::ipc::bindings_builder;
use specta_typescript::Typescript;

fn main() {
    let out = PathBuf::from(env!("CARGO_MANIFEST_DIR"))
        .parent()
        .expect("src-tauri has a parent")
        .join("src/ipc/bindings.ts");

    let builder = bindings_builder();

    builder
        .export(
            Typescript::default()
                .header("// This file is auto-generated by `yarn gen-bindings`. Do not edit.\n"),
            &out,
        )
        .expect("export bindings");

    println!("wrote {}", out.display());
}
